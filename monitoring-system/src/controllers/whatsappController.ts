    const account = await WhatsAppAccountModel.create({
      userId,
      companyName,
      avgResponseTime,
      secondTouch
    });

    // –°–æ–∑–¥–∞–µ–º Docker –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä –¥–ª—è WhatsApp —Å–µ—Å—Å–∏–∏
